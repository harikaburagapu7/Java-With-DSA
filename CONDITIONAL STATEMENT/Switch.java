
public class Switch
{
    public static void main(String args[])
    {
        
        int number = 6;
        
        switch(number){
            case 1 : System.out.println("Somosa");
            break;
            case 2 : System.out.println("Burger");
            break;
            case 3 : System.out.println("Milk Shake");
            break;
            default : System.out.println("Good morning");

        }
    }
}